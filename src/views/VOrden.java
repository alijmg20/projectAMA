    private void Btn_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_GuardarActionPerformed
        String formatoFecha = ("yyyy/MM/dd"); //ESTO ES EL FORMATO   año/mes/fecha
        SimpleDateFormat formateo = new SimpleDateFormat(formatoFecha); //ESTO FORMATEA LA FECHA YA QUE ESTE NO RETORNA UN STRING 

        if (!this.Codigo.getText().equals("") && !this.FEmision.getDatoFecha().toString().isEmpty() && (!this.Bolivares.isSelected() || !this.Dolares.isSelected()) && (!this.Efectivo.isSelected() || !this.Transferencia.isSelected()) && this.Director.getSelectedIndex() > 0 && this.Proveedor.getSelectedIndex() > 0) {
            Date fechaDate = this.FEmision.getDatoFecha();
            String fechaEmString = formateo.format(fechaDate); //Utiliza esta funcion para pasar el formato

            char moneda = ' ';
            if (this.Bolivares.isSelected()) {
                moneda = 'B';
            } else if (this.Dolares.isSelected()) {
                moneda = 'D';
            }

            char pago = ' ';
            if (this.Efectivo.isSelected()) {
                pago = 'E';
            } else if (this.Transferencia.isSelected()) {
                pago = 'T';
            }

            String itemcod = this.Codigo.getText();
            String nombreD = this.Director.getSelectedItem().toString();
            String razonproveedor = this.Proveedor.getSelectedItem().toString();

            this.orden.insertarDatosOrden(itemcod, fechaEmString, moneda, pago, razonproveedor, nombreD);

            this.tableOrden.setModel(this.orden.mostrarDatosOrden());

        } else {
            JOptionPane.showMessageDialog(null, "Por favor rellene los campos faltantes", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_Btn_GuardarActionPerformed

