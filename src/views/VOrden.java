    private void tableOrdenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableOrdenMouseClicked
        this.Codigo.setEnabled(false);
        this.FEmision.setEnabled(true);
        this.FEntrega.setEnabled(true);
        this.Bolivares.setEnabled(true);
        this.Dolares.setEnabled(true);
        this.Efectivo.setEnabled(true);
        this.Transferencia.setEnabled(true);
        this.Director.setEnabled(false);
        this.Proveedor.setEnabled(false);

        try {
            int filaSeleccionada = this.tableOrden.rowAtPoint(evt.getPoint());

            this.Codigo.setText(this.tableOrden.getValueAt(filaSeleccionada, 0).toString());

            DefaultTableModel model = (DefaultTableModel) tableOrden.getModel();
            Date date = new SimpleDateFormat("yyyy/MM/dd").parse((String) model.getValueAt(filaSeleccionada, 1));
            this.FEmision.setDatoFecha(date);

            switch (this.tableOrden.getValueAt(filaSeleccionada, 2).toString()) {
                case "B":
                    this.Bolivares.setSelected(true);
                    break;
                case "D":
                    this.Dolares.setSelected(true);
                    break;
            }
            switch (this.tableOrden.getValueAt(filaSeleccionada, 3).toString()) {
                case "E":
                    this.Efectivo.setSelected(true);
                    break;
                case "T":
                    this.Transferencia.setSelected(true);
                    break;
            }

            String formatoFecha1 = ("yyyy/MM/dd");
            SimpleDateFormat formateo1 = new SimpleDateFormat(formatoFecha1);

            if (this.tableOrden.getValueAt(filaSeleccionada, 4).toString().isEmpty()) {
                Date fecha = new Date(0000 - 00 - 00);
                this.FEntrega.setDatoFecha(fecha);
            } else {
                this.FEntrega.setDatoFecha(formateo1.parse((String) model.getValueAt(filaSeleccionada, 4)));
            }

            this.Director.setSelectedItem(this.tableOrden.getValueAt(filaSeleccionada, 5).toString());
            this.Proveedor.setSelectedItem(this.tableOrden.getValueAt(filaSeleccionada, 6).toString());
        } catch (ParseException ex) {
            Logger.getLogger(VOrden.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tableOrdenMouseClicked

    private void Btn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_EliminarActionPerformed
        int filaSeleccionada = this.tableOrden.getSelectedRow();
        int decision = JOptionPane.showConfirmDialog(null, "seguro que Desea Elimimnar la Orden de Compra ? ");
        if (decision == 0) {
            this.orden.eliminarDatosOrden(this.tableOrden.getValueAt(filaSeleccionada, 0).toString());
            this.tableOrden.setModel(this.orden.mostrarDatosOrden());
        }
    }//GEN-LAST:event_Btn_EliminarActionPerformed

    private void Btn_DetalleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_DetalleActionPerformed
        int filaSeleccionada = this.tableOrden.getSelectedRow();
        if (filaSeleccionada >= 0) {
            String codigoOrden = this.tableOrden.getValueAt(filaSeleccionada, 0).toString();
            int rif = Integer.parseInt(this.tableOrden.getValueAt(filaSeleccionada,7).toString());
            if (!this.tableOrden.getValueAt(filaSeleccionada, 4).toString().equals(username) && this.tipo == 1) {
                JOptionPane.showMessageDialog(null, "Esta cotizacion no te corresponde", "Advertencia", JOptionPane.WARNING_MESSAGE);
            } else {
                new VDetalleOrden(tipo, username, codigoOrden, rif).setVisible(true);
                this.dispose();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Por favor Seleccione una cotizacion ", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_Btn_DetalleActionPerformed

