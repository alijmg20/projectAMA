    private void btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarActionPerformed
        if (this.cbCotizacion.getSelectedIndex() > 0 && this.cbItem.getSelectedIndex() > 0 && this.cbRequisicion.getSelectedIndex() > 0) {
            String nroorden = this.codOrden;
            String codcotizacion = this.cbCotizacion.getSelectedItem().toString();
            int nrorequisicion = Integer.parseInt(this.cbRequisicion.getSelectedItem().toString());
            String nombrei = this.cbItem.getSelectedItem().toString();
            int rif = this.rifProveedor;
            int decision = JOptionPane.showConfirmDialog(null, "Esta seguro que desea actualizar esta informacion ? ");
            if (decision == 0) {
                this.detorden.actualizarDetallesOrdenDeCompra(nroorden, codcotizacion, nrorequisicion, nombrei, rif);
                this.tableDetalleOrden.setModel(detorden.mostrarDetallesOrden(codOrden));
            }

        } else {
            JOptionPane.showMessageDialog(null, "Por favor rellene los campos faltantes", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_actualizarActionPerformed

    private void btn_limpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_limpiarActionPerformed
        this.cbCotizacion.setSelectedIndex(0);
    }//GEN-LAST:event_btn_limpiarActionPerformed

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        int filaSeleccionada = this.tableDetalleOrden.getSelectedRow();
        int decision = JOptionPane.showConfirmDialog(null, "seguro que Desea Elimimnar la Orden de Compra ? ");
        if (decision == 0) {
            String nroorden = this.tableDetalleOrden.getValueAt(filaSeleccionada, 0).toString();
            String codcotizacion = this.tableDetalleOrden.getValueAt(filaSeleccionada, 1).toString();
            int nrorequisicion = Integer.parseInt(this.tableDetalleOrden.getValueAt(filaSeleccionada, 2).toString());
            String codigoi = this.tableDetalleOrden.getValueAt(filaSeleccionada, 3).toString();
            int rif = Integer.parseInt(this.tableDetalleOrden.getValueAt(filaSeleccionada, 5).toString());
            this.detorden.eliminarDatosDetalleOrdenCompra(nroorden,codcotizacion,nrorequisicion,codigoi,rif);
            this.tableDetalleOrden.setModel(this.detorden.mostrarDetallesOrden(codOrden));
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

