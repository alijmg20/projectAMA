    private void cbRequisicionItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbRequisicionItemStateChanged
        if (this.cbRequisicion.getSelectedIndex() > 0) {
            this.cbItem.removeAllItems();
            int nrorequisicion = Integer.parseInt(this.cbRequisicion.getSelectedItem().toString());
            this.detorden.obtenerItemsDetallesOrden(this.cbItem, this.cbCotizacion.getSelectedItem().toString(), nrorequisicion);
        } else {
            this.cbItem.removeAllItems();
        }
    }//GEN-LAST:event_cbRequisicionItemStateChanged

