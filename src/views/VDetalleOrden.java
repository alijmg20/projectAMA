    private void cbCotizacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbCotizacionActionPerformed
        if (this.cbCotizacion.getSelectedIndex() > 0) {
            this.cbRequisicion.removeAllItems();
            String codcotizacion = this.cbCotizacion.getSelectedItem().toString();
            this.detorden.obtenerRequisicionesDetallesOrden(this.cbRequisicion, codcotizacion);
        } else {
            this.cbRequisicion.removeAllItems();
        }
    }//GEN-LAST:event_cbCotizacionActionPerformed

