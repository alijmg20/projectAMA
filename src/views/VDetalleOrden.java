    private void tableDetalleOrdenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDetalleOrdenMouseClicked
        this.cbCotizacion.setEnabled(false);
        this.cbRequisicion.setEnabled(false);
        this.cbItem.setEnabled(true);
        int filaSeleccionada = this.tableDetalleOrden.rowAtPoint(evt.getPoint());
        this.cbCotizacion.setSelectedItem(this.tableDetalleOrden.getValueAt(filaSeleccionada, 1).toString());
        this.cbRequisicion.setSelectedItem(this.tableDetalleOrden.getValueAt(filaSeleccionada, 2).toString());
        this.cbItem.setSelectedItem(this.tableDetalleOrden.getValueAt(filaSeleccionada, 4).toString());
        
        
    }//GEN-LAST:event_tableDetalleOrdenMouseClicked

